async function populateChlLibrary() {
  console.log('Starting ChlLibrary smart population...');
  
  let totalCreated = 0;
  let totalUpdated = 0;
  let totalDeleted = 0;
  let totalErrors = 0;

  try {
    // Step 1: Collect all entries that should exist
    const shouldExist = new Map(); // key: taskInfo+class+period, value: data object
    
    for (const [classLevel, periods] of Object.entries(challengeData)) {
      for (const [period, categories] of Object.entries(periods)) {
        for (const [category, tasks] of Object.entries(categories)) {
          for (const taskString of tasks) {
            try {
              const parsed = JSON.parse(taskString);
              const taskInfo = JSON.stringify(parsed[0]);
              const personalChlDuration = JSON.stringify(parsed[1]);
              
              // Create unique key for this entry
              const uniqueKey = `${taskInfo}|${classLevel}|${period}`;
              
              shouldExist.set(uniqueKey, {
                taskInfo,
                personalChlDuration,
                weekChlDuration: '["C39","5"]',
                challengeType: category,
                class: parseInt(classLevel),
                period
              });
            } catch (error) {
              console.error(`Error parsing task: ${taskString.substring(0, 50)}...`);
              totalErrors++;
            }
          }
        }
      }
    }
    
    console.log(`\nFound ${shouldExist.size} entries in challengeData`);
    
    // Step 2: Get all existing entries from database
    const existingEntries = await prisma.chlLibrary.findMany();
    console.log(`Found ${existingEntries.length} existing entries in database`);
    
    // Step 3: Process each entry that should exist
    for (const [uniqueKey, data] of shouldExist.entries()) {
      // Find if this entry already exists
      const existing = existingEntries.find(entry => 
        entry.taskInfo === data.taskInfo && 
        entry.class === data.class && 
        entry.period === data.period
      );
      
      if (existing) {
        // Check if update is needed
        const needsUpdate = 
          existing.personalChlDuration !== data.personalChlDuration ||
          existing.weekChlDuration !== data.weekChlDuration ||
          existing.challengeType !== data.challengeType;
        
        if (needsUpdate) {
          await prisma.chlLibrary.update({
            where: { id: existing.id },
            data: {
              personalChlDuration: data.personalChlDuration,
              weekChlDuration: data.weekChlDuration,
              challengeType: data.challengeType
            }
          });
          totalUpdated++;
          console.log(`✓ Updated entry ID ${existing.id}`);
        }
      } else {
        // Create new entry
        await prisma.chlLibrary.create({
          data
        });
        totalCreated++;
        console.log(`✓ Created new entry: ${data.taskInfo.substring(0, 40)}...`);
      }
    }
    
    // Step 4: Delete entries that no longer exist in challengeData
    for (const existing of existingEntries) {
      const uniqueKey = `${existing.taskInfo}|${existing.class}|${existing.period}`;
      
      if (!shouldExist.has(uniqueKey)) {
        await prisma.chlLibrary.delete({
          where: { id: existing.id }
        });
        totalDeleted++;
        console.log(`✗ Deleted obsolete entry ID ${existing.id}`);
      }
    }

    console.log('\n=================================');
    console.log('Population complete!');
    console.log(`Total entries created: ${totalCreated}`);
    console.log(`Total entries updated: ${totalUpdated}`);
    console.log(`Total entries deleted: ${totalDeleted}`);
    console.log(`Total errors: ${totalErrors}`);
    console.log('=================================');

  } catch (error) {
    console.error('Fatal error during population:', error);
    return error;
  } finally {
    await prisma.$disconnect();
  }
}

// Run the population script
populateChlLibrary()
  .catch((error) => {
    console.error('Script failed:', error);
    process.exit(1);
  });